let brazilTag = document.querySelector(".brazil-tag")
let brazilImage = document.querySelector(".brazil-img")

brazilTag.addEventListener("mouseover", function(){
   brazilImage.classList.add("zindex");
})

brazilTag.addEventListener("mouseout", function(){
   brazilImage.classList.remove("zindex");
})

